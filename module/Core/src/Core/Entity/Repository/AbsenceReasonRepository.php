namespace Core\Entity\Repository;

use Core\Entity\AbsenceReason;
use Doctrine\ORM\EntityRepository;
use DoctrineORMModule\Paginator\Adapter\DoctrinePaginator;
use Doctrine\ORM\Tools\Pagination\Paginator as ORMPaginator;
use Zend\Paginator\Paginator;
use Exception;
use Zend\Json\Json;
use Doctrine\ORM\Query;

/**
 * Rest API repository for absencereason data.
 *
 * @author Eleri Apsolon <eleri.apsolon@gmail.com>
 */
class AbsenceReasonRepository extends EntityRepository implements CRUD
{

    /**
     * 
     * @param stdClass $params
     * @param mixed $extra
     * @return Paginator
     */
    public function GetList($params = null, $extra = null)
    {
        if ($params) {
            //todo if neccessary
        }
        $dql = "SELECT partial absencereason.{id,name}
                FROM Core\Entity\AbsenceReason absencereason
                WHERE absencereason.trashed IS NULL";
        return new Paginator(
                new DoctrinePaginator(
                new ORMPaginator(
                $this->getEntityManager()
                        ->createQuery($dql)
                        ->setHydrationMode(Query::HYDRATE_ARRAY)
                )
                )
        );
    }

    /**
     * 
     * @param int $id
     * @param bool $returnPartial
     * @param mixed $extra
     * @return int
     */
    public function Get($id, $returnPartial = false, $extra = null)
    {
         if ($returnPartial) {

            $dql = "
                    SELECT 
                        partial absencereason.{id,name}
                    FROM Core\Entity\AbsenceReason absencereason
                    WHERE absencereason.id = :id ";//absencereason is alias

            $q = $this->getEntityManager()->createQuery($dql); //print_r($q->getSQL());
            $q->setParameter('id', $id);
            
            $r = $q->getSingleResult(Query::HYDRATE_ARRAY);
            return $r;
        }
        return $this->find($id);
    }

    /**
     * 
     * @param mixed $data
     * @param bool $returnPartial
     * @param mixed $extra
     * @return AbsenceReason
     * @throws Exception
     */
    public function Create($data, $returnPartial = false, $extra = null)
    {
        
        
        $entity = new AbsenceReason($this->getEntityManager());
        $entity->hydrate($data);

        if (!$entity->validate()) {
            throw new Exception(Json::encode($entity->getMessages(), true));
        }

        $this->getEntityManager()->persist($entity);
        $this->getEntityManager()->flush($entity); // makes DB query, row is saved, id generated by mysql

        if ($returnPartial) {

            $dql = "
                    SELECT 
                        partial absencereason.{id,name}
                    FROM Core\Entity\AbsenceReason absencereason
                    WHERE absencereason.id = " . $entity->getId() . "";

            $q = $this->getEntityManager()->createQuery($dql); //print_r($q->getSQL());
            $r = $q->getSingleResult(Query::HYDRATE_ARRAY);
            return $r;
        }

        return $entity;
    }

    /**
     * 
     * @param int $id
     * @param mixed $data
     * @param bool $returnPartial
     * @param mixed $extra
     * @return AbsenceReason
     * @throws Exception
     */
    public function Update($id, $data, $returnPartial = false, $extra = null)
    {
        $entity = $this->find($id);
        $entity->setEntityManager($this->getEntityManager());
        $entity->hydrate($data);

        if (!$entity->validate()) {
            throw new Exception(Json::encode($entity->getMessages(), true));
        }
        $this->getEntityManager()->persist($entity);
        $this->getEntityManager()->flush($entity);

        if ($returnPartial) {

            $dql = "
                    SELECT 
                        partial absencereason.{id,name}
                    FROM Core\Entity\AbsenceReason absencereason
                    WHERE absencereason.id = :id ";

            $q = $this->getEntityManager()->createQuery($dql); //print_r($q->getSQL());
            $q->setParameter('id', $id);
            
            $r = $q->getSingleResult(Query::HYDRATE_ARRAY);
            return $r;
        }
        return $entity;
    }

    /**
     * 
     * @param int $id
     * @param mixed $extra
     * @return int
     */
    public function Delete($id, $extra = null)
    {
        $this->getEntityManager()->remove($this->find($id));
        $this->getEntityManager()->flush();
        return $id;
    }

}
